/**
 * This is a documentation comment.
 * It explains the 3 types of comments in Java.
 */
public class Comments {

    public static void main(String[] args) {
        // This is a single-line comment. It explains one line of a code.
        System.out.println("Hello, Comments!");

        /* 
         This is a multi-line comment.
         You can write comments here on many lines.
        */
        System.out.println("This program shows 3 types of comments.");
    }
}
